
function countTrees(mapInput, right = 3, down = 1) {
    const lines = mapInput.trim().split('\n');
    const mapHeight = lines.length;
    const mapWidth = lines[0].length;

    let treeCount = 0;
    let x = 0;
    let y = 0;

    while (y < mapHeight) {
        const currentChar = lines[y][x % mapWidth];
        if (currentChar === '#') treeCount++;

        x += right;
        y += down;
    }

    return treeCount;
}


